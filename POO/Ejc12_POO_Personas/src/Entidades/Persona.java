package Entidades;

import java.util.Date;

/**
 *
 * @author drome
 */
public class Persona {
    String name;
    Date born;

    public Persona() {
    }

    public Persona(String name, Date born) {
        this.name = name;
        this.born = born;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getBorn() {
        return born;
    }

    public void setBorn(Date born) {
        this.born = born;
    }

    @Override
    public String toString() {
        return "Persona{" + "name=" + name + ", born=" + born + '}';
    }
    
    
    
    
    
}
